    /**
     * Loads all the settings from the Settings Manager and displays them in
     * form of text or checks.
     */
    @SuppressWarnings("ConvertToTryWithResources")
    private void load() { 
        /*Load available LAFs.*/
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (UIManager.LookAndFeelInfo info : UIManager.getInstalledLookAndFeels()) {
            model.addElement(info.getName());
        }
        jComboBox1.setModel(model);
        
        /*Load Settings*/
        if (settings.useAdvancedUI()) {
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(true);
            loadAdvancedUI = true;
        } else {
            jRadioButton1.setSelected(true);
            jRadioButton2.setSelected(false);
            loadAdvancedUI = false;
        }

        if (settings.getUpdates()) {
            jCheckBox1.setSelected(true);
            checkForUpdates = true;
        } else {
            jCheckBox1.setSelected(false);
            checkForUpdates = false;
        }

        if (settings.saveLogs()) {
            jCheckBox2.setSelected(true);
            saveLogs = true;
        } else {
            jCheckBox3.setSelected(false);
            saveLogs = false;
        }

        if (settings.checkForDevicesOnStartup()) {
            jCheckBox3.setSelected(true);
            detectDevices = true;
        } else {
            jCheckBox3.setSelected(false);
            detectDevices = false;
        }

        if (settings.autoLoadDeviceInfo()) {
            jCheckBox4.setSelected(true);
            loadDeviceInfo = true;
        } else {
            jCheckBox4.setSelected(false);
            loadDeviceInfo = false;
        }
        
        jComboBox1.setSelectedItem(settings.getLookAndFeel());
        
        Language[] installedLangs = Language.values();
        DefaultComboBoxModel _model = new DefaultComboBoxModel();
        for (int i = 0; i  < installedLangs.length; i++) {
            _model.addElement(installedLangs[i]);
        }
        jComboBox2.setModel(_model);

        try {
            /*Load Version Info*/
            BufferedReader reader = new BufferedReader(new FileReader(this.getClass().getResource("/eu/m4gkbeatz/uat.ver").getFile()));
            @SuppressWarnings("UnusedAssignment")
            String line = null;
            while ((line = reader.readLine()) != null) {
                if (line.startsWith("\tProgram version: ")) {
                    String[] arr = line.split(": ");
                    jTextField1.setText(arr[1]);
                }
                if (line.startsWith("\tOfficial Build: ")) {
                    String[] arr = line.split(": ");
                    jTextField2.setText(arr[1]);
                }
            }
            reader.close();
        } catch (IOException ex) {
            if (ex.toString().toLowerCase().contains("not found")) {
                jTextField1.setText("Unknown");
                jTextField2.setText("False. All warranties are void.");
            } else {
                log.log(LogLevel.SEVERE, "ERROR: Error loading version info!\n" + ex.toString());
                jTextField1.setText("n/a");
                jTextField2.setText("n/a");
            }
        }
    }

