    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        settings.setAutoLoadDeviceInfo(loadDeviceInfo);
        settings.setCheckForDevicesOnStartup(detectDevices);
        settings.setSaveLogs(saveLogs);
        settings.setGetUpdates(checkForUpdates);
        settings.setUseAdvancedUI(loadAdvancedUI);
        settings.setLookAndFeel(jComboBox1.getSelectedItem().toString());
        settings.setLang(Language.valueOf(jComboBox2.getSelectedItem().toString()));
        try {
            settings.saveSettings();
        } catch (IOException ex) {
            log.log(LogLevel.SEVERE, "ERROR: Error while saving program settings!\n" + ex.toString());
        }
        JOptionPane.showMessageDialog(null, "INFORMATION: Your settings have been saved, however,\n"
                + "some (or all) of these will only become active, once the program has been restarted.", "Restart Required.", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_formWindowClosing

